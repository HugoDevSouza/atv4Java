package atv4;

public class Calculadora {

	public static int somarDoisNumeros(int numeroUm, int numeroDois) {
		return numeroUm + numeroDois;
	}

	public static int subtrairDoisNumeros(int numeroUm, int numeroDois) {
		return numeroUm - numeroDois;
	}

	public static int multiplicarDoisNumeros(int numeroUm, int numeroDois) {
		return numeroUm * numeroDois;
	}

	public static int dividirDoisNumeros(int numeroUm, int numeroDois) {
		return numeroUm / numeroDois;
	}
}
